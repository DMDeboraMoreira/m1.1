//Scape Sequence (secuencia de escape)
//Es una combinación de caracteres que representa un carácter especial en un string, 
//como un salto de línea o un tabulador, y comienza con una barra invertida (\). 
//Por ejemplo, '\n' representa un salto de línea y '\t' un tabulador
//Ejemplo:
const texto = 'Hola\nMundo';
console.log(texto);
// Output:
// Hola
// Mundo